package Operators;

public class BitwiseOperators {
    public static void main(String[] args)
    {
        int a = 5;
        int b =9 ;
        System.out.println("a&b = " + (a & b)); //bitwise and
        System.out.println("a|b = " + (a | b)); //bitwise or
        System.out.println("a^b = " + (a ^ b)); // bitwise xor
        System.out.println("~a = " + (b));

    }

}
